package com.hildebrando.visado.dto;

public class CombinacionSolicitudUtil {
	
	
	private String estado;
	private String codSoli;
	private int numCombinaciones;
	private int numGrupo;
	
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCodSoli() {
		return codSoli;
	}
	public void setCodSoli(String codSoli) {
		this.codSoli = codSoli;
	}
	public int getNumCombinaciones() {
		return numCombinaciones;
	}
	public void setNumCombinaciones(int numCombinaciones) {
		this.numCombinaciones = numCombinaciones;
	}
	public int getNumGrupo() {
		return numGrupo;
	}
	public void setNumGrupo(int numGrupo) {
		this.numGrupo = numGrupo;
	}

}
